public class DataType {
    public static void main(String[] args) {
        double a = 3.1416;
        byte b = 127;
        System.out.println("double - переменная типа с плавающей точкой с наибольшим диапазоном чисел = " + a);
        System.out.print("byte - переменная целочисленного типа с наименьшим диапазоном чисел = " + b);
    }
}
